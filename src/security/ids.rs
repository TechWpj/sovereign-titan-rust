    #[test]
    fn test_alert_dedup() {
        let mut engine = IDSEngine::new();
        // Generate two identical exfiltration scenarios in quick succession.
        let connections: Vec<ConnectionInfo> = (0..60)
            .map(|i| ConnectionInfo {
                local_addr: "192.168.1.100".into(),
                local_port: 50000 + i,
                remote_addr: "10.0.0.1".into(),
                remote_port: 443,
                process_name: "badapp".into(),
                status: "ESTABLISHED".into(),
                direction: "outbound".into(),
                is_encrypted: false,
            })
            .collect();
        let report1 = engine.analyze(&connections, None);
        let report2 = engine.analyze(&connections, None);
        // Second report should have the same alert deduped.
        let beh003_1 = report1.alerts.iter().filter(|a| a.rule_id == "BEH-003").count();
        let beh003_2 = report2.alerts.iter().filter(|a| a.rule_id == "BEH-003").count();
        assert!(beh003_1 > 0);
        assert_eq!(beh003_2, 0, "Duplicate alert should be suppressed by cooldown");
    }
